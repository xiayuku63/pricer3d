def generate_prusa_config(
    layer_height: float = 0.2,
    infill_percent: int = 20,
    perimeters: int = 3,
    top_shell_layers: int = 5,
    bottom_shell_layers: int = 5,
    material_density: float = 1.24,
) -> str:
    """
    Generate a PrusaSlicer-compatible INI configuration snippet.
    
    This creates minimal config overrides to match the quoting parameters.
    Returns the path to a temporary config file.
    """
    # Create a temporary INI file
    fd, path = tempfile.mkstemp(suffix=".ini", prefix="prusa_")
    with os.fdopen(fd, "w") as f:
        f.write("""# Generated by prusa_slicer.py for 3D Printing Quoting System
# Print settings
layer_height = {layer_height}
fill_density = {infill}%
perimeters = {perimeters}
top_shell_layers = {top}
bottom_shell_layers = {bottom}

# Printer settings (Bambu Lab A1 0.4mm equivalent)
bed_shape = 256x256
bed_size = 256,256
print_center = 128,128
z_offset = 0
nozzle_diameter = 0.4
max_print_z = 256

# Acceleration (Bambu A1: 12,000 mm/s²)
default_acceleration = 10000
perimeter_acceleration = 8000
infill_acceleration = 12000
bridge_acceleration = 3000
first_layer_acceleration = 2000

# Speed settings (Bambu A1 equivalents)
outer_perimeter_speed = 200
inner_perimeter_speed = 300
small_perimeter_speed = 150
solid_infill_speed = 250
top_solid_infill_speed = 200
support_material_speed = 200
travel_speed = 500
first_layer_speed = 50
max_volumetric_speed = 12

# Filament settings (Bambu PLA Basic)
filament_diameter = 1.75
filament_density = {density}
extrusion_multiplier = 0.98
temperature = 220
first_layer_temperature = 220
bed_temperature = 55
first_layer_bed_temperature = 55

# Cooling
enable_fan = 1
fan_always_on = 1
fan_below_layer_time = 30
slowdown_below_layer_time = 10
min_print_speed = 25

# Quality
resolution = 0.0125
""".format(
            layer_height=layer_height,
            infill=infill_percent,
            perimeters=perimeters,
            top=top_shell_layers,
            bottom=bottom_shell_layers,
            density=material_density,
        ))
    return path


